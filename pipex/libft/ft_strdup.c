#include "libft.h"
#include <stdlib.h>

static char	*ft_strcpy(char *dest, const char *src)
{
	size_t	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s)
{
	char	*duplicated;

	duplicated = (char *)malloc(ft_strlen(s) + 1);
	if (duplicated == NULL)
		return (NULL);
	ft_strcpy(duplicated, s);
	return (duplicated);
}
